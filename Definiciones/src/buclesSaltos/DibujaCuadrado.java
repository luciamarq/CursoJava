package buclesSaltos;

public class DibujaCuadrado {
	/*
	 * 
	 * REPASAR:
	 */
	public static void main(String[] args) {
		
		int pos=8;
		int fila; 
		int columna;
		
		for(fila=1; fila<=pos; fila++) {
			for(columna=1; columna <=pos; columna++) {
				System.out.print("*  ");
			}
			System.out.println("");
		}
		
		

	}

}
